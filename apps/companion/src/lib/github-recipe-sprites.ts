const recipeSpriteByFoodId = new Map<number, (typeof GITHUB_RECIPE_SPRITES)[number]>(
  GITHUB_RECIPE_SPRITES.map((entry) => [entry.foodId, entry]),
);

const recipeSpriteByRecipeId = new Map<number, (typeof GITHUB_RECIPE_SPRITES)[number]>(
  GITHUB_RECIPE_SPRITES.map((entry) => [entry.recipeId, entry]),
);

const recipeSpriteByName = new Map<string, (typeof GITHUB_RECIPE_SPRITES)[number]>(
  GITHUB_RECIPE_SPRITES.map((entry) => [entry.name, entry]),
);

export function resolveGithubRecipeSprite(recipe: {
  id?: number | null;
  recipeId?: number | null;
  name?: string | null;
}) {
  if (typeof recipe.id === 'number') {
    const byFoodId = recipeSpriteByFoodId.get(recipe.id);
    if (byFoodId) return byFoodId;
  }

  if (typeof recipe.recipeId === 'number') {
    const byRecipeId = recipeSpriteByRecipeId.get(recipe.recipeId);
    if (byRecipeId) return byRecipeId;
  }

  const normalizedName = recipe.name?.trim();
  return normalizedName ? recipeSpriteByName.get(normalizedName) ?? null : null;
}
